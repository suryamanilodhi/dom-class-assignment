function fun1(){
   
    if( document.getElementById('pane').style.display=="none"){
     document.getElementById('pane').style.display="block";
     document.getElementById('head1').style.backgroundColor='blue';
    }
    else{
     document.getElementById('pane').style.display="none";
     document.getElementById('head1').style.backgroundColor='white';
    }
   
 } 
 
 
